


public class  DataTypes{

    public static void main(String[] args) {

        int number = 10;
        System.out.println(number);  //int datatype for small integer numbers

        String b = "anjali";
        System.out.println(b);  //string datatype for collection of characters

        double doub = 255.67898D;
        System.out.println(doub);  //double datatype for long decimal numbers

        char a = '#';
        System.out.println(a);  //char datatype for a single character

        boolean bool = true;
        System.out.println(bool);  //boolean datatype for true of false statement

        long l = 35676286332L;
        System.out.println(l);  //long datatype for long integer numbers

        short s = 3654;
        System.out.println(s);  //short datatype for short integer numbers
 
        float f = 36.54F;
        System.out.println(f);  //float datatype for small decimal numbers
    }
}
